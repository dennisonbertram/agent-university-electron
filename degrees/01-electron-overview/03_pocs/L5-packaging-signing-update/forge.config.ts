/**
 * Electron Forge configuration — L5.
 *
 * Surfaces:
 *   - packagerConfig: appBundleId, appCategoryType, asar, ignore (so dev-
 *     only tooling stays out of the bundle), protocols (electron-l5),
 *     extendInfo merged from `Info.plist.template`, conditional osxSign +
 *     osxNotarize.
 *   - makers: MakerDMG + MakerZIP, both darwin (universal supported via
 *     `npm run make:universal` which forge handles by re-running per arch
 *     and merging via `@electron/universal`).
 *   - plugins: FusesPlugin with the full hardening matrix.
 *   - hooks: packageAfterCopy → emits the simulated-signing.md file +
 *     skip-log marker when no APPLE_ID is in the environment (BT-L5-5).
 *
 * REF-05 (poc-selection): npm is the pinned package manager. Forge is
 * driven exclusively through `npm run package` / `npm run make`.
 */
import type { ForgeConfig } from '@electron-forge/shared-types'
import { MakerDMG } from '@electron-forge/maker-dmg'
import { MakerZIP } from '@electron-forge/maker-zip'
import { FusesPlugin } from '@electron-forge/plugin-fuses'
import { FuseV1Options, FuseVersion } from '@electron/fuses'
import path from 'node:path'
import { readFileSync, writeFileSync, existsSync, mkdirSync } from 'node:fs'
import plist from 'plist'

const POC_ROOT = __dirname
const INFO_TEMPLATE_PATH = path.join(POC_ROOT, 'Info.plist.template')

function readInfoTemplateExtendInfo(): Record<string, unknown> {
  if (!existsSync(INFO_TEMPLATE_PATH)) return {}
  try {
    const parsed = plist.parse(readFileSync(INFO_TEMPLATE_PATH, 'utf8'))
    if (typeof parsed === 'object' && parsed !== null) {
      return parsed as Record<string, unknown>
    }
  } catch {
    // ignore — fall through to empty
  }
  return {}
}

const HAS_APPLE_CREDS =
  typeof process.env.APPLE_ID === 'string' &&
  process.env.APPLE_ID.length > 0 &&
  typeof process.env.APPLE_TEAM_ID === 'string' &&
  process.env.APPLE_TEAM_ID.length > 0 &&
  typeof process.env.APPLE_APP_SPECIFIC_PASSWORD === 'string' &&
  process.env.APPLE_APP_SPECIFIC_PASSWORD.length > 0

const SIMULATED_MD_PATH = path.join(POC_ROOT, 'simulated-signing.md')
const SKIP_LOG_PATH = path.join(POC_ROOT, 'test-results', 'packaging-skip.log')

function writeSimulatedSigningArtifacts(): void {
  const md = `# Simulated Signing — what would happen with real credentials

This file is auto-generated by \`forge.config.ts\`'s \`packageAfterCopy\` hook
whenever \`npm run package\` runs without Apple Developer credentials in the
environment. Its presence is the deterministic test signal for BT-L5-5:
no \`APPLE_ID\`/\`APPLE_TEAM_ID\`/\`APPLE_APP_SPECIFIC_PASSWORD\` ⇒ signing
+ notarization are skipped ⇒ this file is written ⇒ the skip-log marker
\`packaging:signing:skipped:no-credentials\` is also produced.

Skip-log token (the test grep target): \`packaging:signing:skipped:no-credentials\`

## What \`forge\` did (this run)

1. Bundled \`dist/\` into \`out/<platform>-<arch>/<App>.app/Contents/Resources/app.asar\`.
2. Applied \`FusesPlugin\` (RunAsNode=false, EnableNodeOptionsEnvironmentVariable=false,
   EnableCookieEncryption=true, OnlyLoadAppFromAsar=true,
   EnableEmbeddedAsarIntegrityValidation=true,
   EnableNodeCliInspectArguments=false).
3. Merged \`Info.plist.template\` into the final \`Info.plist\`
   (CFBundleURLTypes for \`electron-l5://\`, CFBundleShortVersionString,
   NSHumanReadableCopyright).
4. SKIPPED osxSign — no \`APPLE_ID\` in env.
5. SKIPPED osxNotarize — no \`APPLE_ID\` in env.

The resulting bundle is launchable by Playwright (BT-L5-10) but is NOT
distributable: macOS Gatekeeper will refuse to open it on a clean machine
("App is damaged" / "cannot verify developer").

## What would happen with real credentials

With \`APPLE_ID\`, \`APPLE_TEAM_ID\`, and \`APPLE_APP_SPECIFIC_PASSWORD\` set:

1. \`@electron/osx-sign\` signs every executable, framework, helper, and
   embedded dylib in the bundle with the Developer ID Application
   certificate.

   Equivalent manual command (per file):
   \`\`\`
   codesign --force --options runtime \\
     --entitlements entitlements.mac.plist \\
     --sign "Developer ID Application: <name> (<teamId>)" \\
     out/<platform>-<arch>/<App>.app/Contents/MacOS/<exe>
   \`\`\`

2. Forge then re-applies fuses to the signed binary (fuse-flipping changes
   bytes; the post-flip binary must be re-signed). This is automatic via
   \`FusesPlugin\`.

3. \`@electron/notarize\` zips the signed bundle and submits to Apple's
   notarization service. Equivalent manual command:
   \`\`\`
   xcrun notarytool submit out/<App>.zip \\
     --apple-id "$APPLE_ID" \\
     --team-id "$APPLE_TEAM_ID" \\
     --password "$APPLE_APP_SPECIFIC_PASSWORD" \\
     --wait
   \`\`\`
   Timing: 30s to 30 minutes (typically 1-3 minutes in 2026).

4. On success, the ticket is stapled to the bundle:
   \`\`\`
   xcrun stapler staple out/<platform>-<arch>/<App>.app
   xcrun stapler validate out/<platform>-<arch>/<App>.app
   \`\`\`

5. The DMG made AFTER stapling inherits the staple. Verification:
   \`\`\`
   codesign --verify --deep --strict out/<App>.app
   spctl --assess --type exec --verbose out/<App>.app
   \`\`\`

## What \`R-L5-4\` enforces

The skip path must REMAIN conditional. If a future commit changes
\`forge.config.ts\` to call \`osxSign\` unconditionally, the regression test
fails (no \`process.env.APPLE_ID\` reference near \`osxSign\`/\`osxNotarize\`).
This protects against a CI run silently succeeding by signing with the
wrong key.

## See also

- 17-code-signing-notarization.md (full simulated-vs-real comparison).
- environment.md ("Apple Developer Account: NOT available").
- decision-log.md (which version of this POC promoted the skip path from
  documented to enforced).
`
  writeFileSync(SIMULATED_MD_PATH, md, 'utf8')
  try {
    mkdirSync(path.dirname(SKIP_LOG_PATH), { recursive: true })
    writeFileSync(
      SKIP_LOG_PATH,
      `${new Date().toISOString()} packaging:signing:skipped:no-credentials reason="no APPLE_ID/APPLE_TEAM_ID/APPLE_APP_SPECIFIC_PASSWORD in env"\n`,
      'utf8',
    )
  } catch {
    // best-effort; the simulated-signing.md file is the load-bearing signal
  }
  // Mirror the skip log to stdout so a human running forge sees the event.
  // eslint-disable-next-line no-console
  console.log(
    'packaging:signing:skipped:no-credentials — see simulated-signing.md for the real-signing flow.',
  )
}

const baseExtendInfo = readInfoTemplateExtendInfo()

const config: ForgeConfig = {
  packagerConfig: {
    appBundleId: 'com.agentuniversity.l5.packaging-signing-update',
    appCategoryType: 'public.app-category.productivity',
    name: 'L5-packaging-signing-update',
    executableName: 'L5-packaging-signing-update',
    asar: true,
    // Aggressively exclude dev-only directories from the packaged bundle so
    // `app.asar` stays small and so test fixtures don't ship with the app.
    ignore: [
      /^\/tests\b/,
      /^\/test-results\b/,
      /^\/out\b/,
      /^\/scripts\/fixtures\b/,
      /^\/\.git\b/,
      /^\/\.gitignore\b/,
      /^\/forge\.config\.ts$/,
      /^\/playwright\.config\.ts$/,
      /^\/vitest\.config\.ts$/,
      /^\/tsconfig.*\.json$/,
      /^\/Info\.plist\.template$/,
      /^\/entitlements\.mac\.plist$/,
      /^\/poc-report\.md$/,
      /^\/test-plan\.md$/,
      /^\/README\.md$/,
      /^\/simulated-signing\.md$/,
      /^\/src\/.*\.ts$/, // source TS — only dist/ is needed
    ],
    protocols: [
      {
        name: 'L5 Packaging Signing Update',
        schemes: ['electron-l5'],
      },
    ],
    extendInfo: baseExtendInfo,
    // Conditional osxSign + osxNotarize — gated on env (BT-L5-5, R-L5-4).
    ...(HAS_APPLE_CREDS
      ? {
          osxSign: {
            optionsForFile: (_filePath: string): unknown => ({
              entitlements: path.join(POC_ROOT, 'entitlements.mac.plist'),
              hardenedRuntime: true,
              'gatekeeper-assess': false,
            }),
          },
          osxNotarize: {
            appleId: process.env.APPLE_ID!,
            appleIdPassword: process.env.APPLE_APP_SPECIFIC_PASSWORD!,
            teamId: process.env.APPLE_TEAM_ID!,
          },
        }
      : {}),
  },
  rebuildConfig: {},
  makers: [
    new MakerDMG({
      format: 'ULFO',
      // The DMG window cosmetics are intentionally minimal — the test only
      // asserts the artifact exists.
    }),
    new MakerZIP({}, ['darwin']),
  ],
  plugins: [
    new FusesPlugin({
      version: FuseVersion.V1,
      [FuseV1Options.RunAsNode]: false,
      [FuseV1Options.EnableCookieEncryption]: true,
      [FuseV1Options.EnableNodeOptionsEnvironmentVariable]: false,
      [FuseV1Options.EnableNodeCliInspectArguments]: false,
      [FuseV1Options.EnableEmbeddedAsarIntegrityValidation]: true,
      [FuseV1Options.OnlyLoadAppFromAsar]: true,
    }),
  ],
  hooks: {
    /**
     * `packageAfterCopy` runs after Forge has copied the app source into the
     * packaging staging dir and BEFORE @electron/packager invokes osxSign /
     * osxNotarize. We use it to:
     *   1. Emit `simulated-signing.md` whenever we're on the skip path
     *      (HAS_APPLE_CREDS === false).
     *   2. Write the skip-log marker file so the e2e test has a deterministic
     *      grep target.
     */
    packageAfterCopy: async (_forgeConfig, _buildPath) => {
      if (!HAS_APPLE_CREDS) {
        writeSimulatedSigningArtifacts()
      }
    },
  },
}

export default config
